
// Class representing Zones within the Zoo
public class Zone {
    private String zoneID;
    private String zoneName;

    public Zone(String zoneID, String zoneName) {
        this.zoneID = zoneID;
        this.zoneName = zoneName;
    }

    public String getZoneID() {
        return zoneID;
    }

    public String getZoneName() {
        return zoneName;
    }
}

